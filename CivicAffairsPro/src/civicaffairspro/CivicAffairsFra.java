    private void checkNumericFild(Character c, JTextField jtf) {//takes 'c' and 'jtf' from keyTyped event that put on jTextFields
        int v = 0;
        char errors[] = (jtf.getText()).toCharArray();


        for (int i = 0; i < errors.length; i++) {
            if (!(Character.isDigit(errors[i]))) {
                v = 1;
            }
        }


        if (v == 0) {
            jtf.setBorder(new MatteBorder(1, 1, 1, 1, Color.GRAY));
            jLabel23.setText("");
            if(jtf==jTextField2)unNumIdField=false;
            if(jtf==jTextField10)unNumDateField=false;
            if(jtf==jTextField5)unNumBirthField=false;
            if(jtf==jTextField7)unNumConstraintField=false;
            
        } else {
            jtf.setBorder(new MatteBorder(1, 1, 1, 1, Color.red));
            jLabel23.setText("أدخل أرقام فقط");
            if(jtf==jTextField2)unNumIdField=true;
            if(jtf==jTextField10)unNumDateField=true;
            if(jtf==jTextField5)unNumBirthField=true;
            if(jtf==jTextField7)unNumConstraintField=true;
           
        }
        if(unNumBirthField==true||unNumConstraintField==true||unNumDateField==true||unNumIdField==true)unNumeric=true;else unNumeric=false;
    }

